  m <- matrix(0, nrow = 10, ncol = 10,
              dimnames = list(labels, labels))

  # [row=to, col=from] = coefficient
  m["x0", "x3"] <-  3.0
  m["x2", "x3"] <-  6.0
  m["x9", "x3"] <-  7.0
  m["x1", "x0"] <-  3.0
  m["x5", "x0"] <-  4.0
  m["x4", "x0"] <-  8.0
  m["x7", "x0"] <-  3.0
  m["x1", "x2"] <-  2.0
  m["x4", "x2"] <- -1.0
  m["x8", "x2"] <-  0.5
  m["x6", "x1"] <-  2.0
  m["x8", "x5"] <-  2.0
  m["x7", "x4"] <-  1.5
  m["x9", "x6"] <-  1.0

  return(m)
}
